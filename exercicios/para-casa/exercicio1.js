var nota1 = parseInt(prompt("Coloque a primeira nota:"))
var nota2 = parseInt(prompt("Coloque a segunda nota:"))
var media = (nota1 + nota2)/2

if (media >=7) {
    alert("você foi aprovado com a média " + media)
} else if (media >= 5) {
    alert("você está em recuperação com a média " + media)
} else{
    alert("você foi reprovado, média " + media)
}


    

